int		ft_iterative_factorial(int nb)
{
	int i;

	if (nb > 12)
	{
		return (0);
	}
	if (nb < 0)
	{
		return (0);
	}
	i = 1;
	while (1 <= nb)
	{
		i = nb * i;
		nb--;
	}
	return (i);
}
